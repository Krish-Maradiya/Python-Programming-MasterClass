# Tuple Examples

numbers = (10, 20, 30, 40)

print("Tuple:", numbers)

# Access element
print("First element:", numbers[0])

# Length
print("Length:", len(numbers))

# Loop
for num in numbers:
    print(num)

# Count
print("Count of 20:", numbers.count(20))

# Index
print("Index of 30:", numbers.index(30))