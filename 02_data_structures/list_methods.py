# List Methods

numbers = [10, 20, 30, 40]

print("Original list:", numbers)

# Append
numbers.append(50)
print("After append:", numbers)

# Insert
numbers.insert(1, 15)
print("After insert:", numbers)

# Remove
numbers.remove(30)
print("After remove:", numbers)

# Pop
numbers.pop()
print("After pop:", numbers)

# Sort
numbers.sort()
print("Sorted list:", numbers)

# Reverse
numbers.reverse()
print("Reversed list:", numbers)