# Examples of operators in Python

a = 10
b = 5

# Arithmetic Operators
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)

# Comparison Operators
print("a > b:", a > b)
print("a < b:", a < b)
print("a == b:", a == b)

# Logical Operators
x = True
y = False

print("AND:", x and y)
print("OR:", x or y)
print("NOT:", not x)