# data_types.py
# Examples of Python data types

# Integer
age = 25
print("Age:", age, type(age))

# Float
price = 19.99
print("Price:", price, type(price))

# String
name = "Python"
print("Name:", name, type(name))

# Boolean
is_active = True
print("Is Active:", is_active, type(is_active))

# List
numbers = [1, 2, 3, 4]
print("List:", numbers, type(numbers))

# Tuple
coordinates = (10, 20)
print("Tuple:", coordinates, type(coordinates))

# Dictionary
person = {"name": "Krish", "age": 21}
print("Dictionary:", person, type(person))